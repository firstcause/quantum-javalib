package com.denizensoft.jlib;

public class TypeErrorException extends LibException
{
	public TypeErrorException()
	{
	}

	public TypeErrorException(String arg0)
	{
		super(arg0);
	}

	public TypeErrorException(Throwable arg0)
	{
		super(arg0);
	}

	public TypeErrorException(String arg0, Throwable arg1)
	{
		super(arg0, arg1);
	}

}
